# একটি কম্পিউটার প্রোগ্রাম হলো কম্পিউটারের দ্বারা "কার্যকর" করার জন্য "নির্দেশাবলীর" একটি তালিকা। একটি প্রোগ্রামিং ভাষায়, এই প্রোগ্রামিং নির্দেশাবলীকে স্টেটমেন্ট বলা হয়। নিম্নলিখিত স্টেটমেন্টটি স্ক্রিনে "Python is fun!" লেখাটি প্রিন্ট করে:
print("Python is fun!")
# পাইথনে, একটি স্টেটমেন্ট সাধারণত লাইনের শেষে শেষ হয়। অন্যান্য অনেক প্রোগ্রামিং ভাষার (যেমন, জাভা বা সি) মতো এখানে সেমিকোলন (;) ব্যবহার করার প্রয়োজন হয় না।

print("Hello World!")
print("Have a good day.")
print("Learning Python is fun!")
# The first statement is executed first (print "Hello World!").
# Then the second statement is executed (print "Have a good day.").
# And at last, the third statement is executed (print "Learning Python is fun!").


# Semicolons
print("Hello"); print("How are you?"); print("Bye bye!")
# পাইথনে সেমিকোলনের একটি বিশেষ কাজ আছে। আপনি যদি একই লাইনে একাধিক কমান্ড বা স্টেটমেন্ট লিখতে চান, তখন সেমিকোলন ব্যবহার করা হয়।
# এখানে পাইথন সেমিকোলন দেখে বুঝতে পারে যে, এক লাইনের ভেতরেই আসলে তিনটি আলাদা আলাদা কমান্ড আছে।
# Best practice: Put each statement on its own line so your code is easy to understand.


