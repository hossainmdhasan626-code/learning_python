# Join List 

# Join Two Lists

# There are several ways to join, or concatenate, two or more lists in Python.
# One of the easiest ways are by using the + operator.
list_1_1 = ["a","b","c"]
list_1_2 = [1,2,3,]
list_1_3 = list_1_1 + list_1_2
print(list_1_3)


# Another way to join two lists is by appending all the items from list2 into list1, one by one:

list_2_1 = ["a","b","c"]
list_2_2 = [1,2,3]

for x in list_2_2 :
    list_2_1.append(x)
print(list_2_1)

list_3_1 = ["a","b","c"]
list_3_2 = [1,2,3]
for x in range(len(list_3_2)):
    list_3_1.insert(x,list_3_2[x])
print(list_3_1)


# extend()

# Or you can use the extend() method, where the purpose is to add elements from one list to another list:

list_4_1 = ["a", "b" , "c"]
list_4_2 = [1, 2, 3]
list_4_1.extend(list_4_2)
print(list_4_1)

# Asterisk Unpacking

list_5_1 = ["a", "b" , "c"]
list_5_2 = [1, 2, 3]
list_5_3 = [*list_5_2 , *list_5_1]
print(list_5_3)


# ১. + অপারেটর ব্যবহার করা (Concatenation)
# এটি সবচেয়ে সহজ এবং সাধারণ উপায়। দুটি লিস্টকে জাস্ট যোগ চিহ্নের দুই পাশে বসিয়ে দিলেই কাজ শেষ।
list_1 = [1, 2]
list_2 = [3, 4]
combined_list = list_1 + list_2  # [1, 2, 3, 4]
# মেমোরির খেলা: এটি মেমোরিতে সম্পূর্ণ নতুন একটি লিস্ট অবজেক্ট তৈরি করে। list1 বা list2 এর কোনো পরিবর্তন হয় না।
# কখন ব্যবহার করবেন: যখন আপনার আসল লিস্ট দুটিকে অক্ষত রেখে সম্পূর্ণ নতুন একটি থার্ড লিস্টের প্রয়োজন হবে।


# ২. extend() মেথড ব্যবহার করা (In-place Modify)
# এটি একটি লিস্টের পেছনে আরেকটি লিস্টের সব উপাদানকে এনে ঢুকিয়ে দেয়।
list_1_1 = [1, 2]
list_1_2 = [3, 4]
list_1_1.extend(list_1_2)  # list1 এখন হয়ে যাবে [1, 2, 3, 4]
# মেমোরির খেলা: এটি মেমোরিতে নতুন কোনো লিস্ট বানায় না। এটি list1 এর ভেতরেই সরাসরি পরিবর্তন (In-place) করে মেমোরি বাঁচায়।
# কখন ব্যবহার করবেন: যখন আপনার নতুন লিস্টের দরকার নেই, বরং প্রথম লিস্টটার ভেতরেই অন্য লিস্টের ডেটা ঢুকিয়ে বড় করতে চান। এটি মেমোরির দিক থেকে বেশ সাশ্রয়ী।

# ৩. for লুপের সাথে append() ব্যবহার করা (ম্যানুয়াল উপায়)
# আপনি যেটা জানতে চেয়েছেন—লুপ ঘুরিয়ে একটা একটা করে উপাদান অন্য লিস্টে পাঠানো।
list_2_1 = [1, 2]
list_2_2 = [3, 4]
for item in list_2_2:
    list_2_1.append(item)  # list1 এখন [1, 2, 3, 4]
# মেমোরির খেলা: এটিও extend() এর মতোই list1-কে ইন-প্লেস মডিফাই করে।
# সমস্যা ও তফাত: এটি সবচেয়ে ধীরগতির এবং অনুচিত একটি পদ্ধতি। কারণ extend() মেথডটি ব্যাকগ্রাউন্ডে অত্যন্ত দ্রুত সি-কোডে (C-Code) একবারে পুরো লিস্ট কপি করে ফেলে। আর for লুপ ব্যবহার করলে পাইথনকে প্রতিটা উপাদানের জন্য আলাদা আলাদা করে লুপের চক্কর কাটতে হয় এবং বারবার append() ফাংশন কল করতে হয়, যা বড় লিস্টের ক্ষেত্রে কোডের পারফরম্যান্স একদম কমিয়ে দেয়।

# ৪. Asterisk * অপারেটর (Unpacking) — পাইথনিক স্টাইল
# পাইথন ৩.৫+ সংস্করণে আসা এটি একটি চমৎকার আধুনিক উপায়।
list_3_1 = [1, 2]
list_3_2 = [3, 4]
combined_list = [*list_3_1, *list_3_2]  # [1, 2, 3, 4]
# মেমোরির খেলা: + অপারেটরের মতোই এটিও সম্পূর্ণ নতুন একটি লিস্ট অবজেক্ট তৈরি করে।
# সুবিধা: এটি অত্যন্ত ফাস্ট এবং এটি দিয়ে আপনি চাইলে দুইয়ের অধিক লিস্ট বা লিস্টের মাঝে অন্য কোনো ছিটফুট উপাদানও একসাথে জোড়া দিতে পারেন (যেমন: [0, *list1, 99, *list2])।


# 🎯 আপনার কোনটা ব্যবহার করা উচিত?
# ১. আপনার যদি নতুন একটি লিস্টের প্রয়োজন হয় এবং আসল লিস্ট দুটিকে অপরিবর্তিত রাখতে চান, তবে চোখ বন্ধ করে [*list1, *list2] অথবা list1 + list2 ব্যবহার করুন।
# ২. আপনার যদি নতুন লিস্টের দরকার না থাকে, বরং আসল লিস্টেরই সাইজ বাড়াতে চান, তবে পারফরম্যান্স ও মেমোরি বাঁচানোর জন্য list1.extend(list2) ব্যবহার করা সবচেয়ে বুদ্ধিমানের কাজ। 
# লুপ ঘুরিয়ে append() করতে যাবেন না, যদি না সেখানে কোনো ফিল্টারিংয়ের (if condition) ব্যাপার থাকে।
